#!/usr/bin/env bash

###############################################################################
#
# Monitoring-Grafana Project
#
# File:
#   scripts/lib/logging.sh
#
# Description:
#   Common logging functions used by all Monitoring-Grafana scripts.
#
###############################################################################
# Internal logging functions
###############################################################################

log_info() {

    printf "${COLOR_BLUE}[INFO]${COLOR_RESET} %s\n" "$*"
}

log_success() {

    printf "${COLOR_GREEN}[ OK ]${COLOR_RESET} %s\n" "$*"
}

log_warn() {

    printf "${COLOR_YELLOW}[WARN]${COLOR_RESET} %s\n" "$*"
}

log_error() {

    printf "${COLOR_RED}[FAIL]${COLOR_RESET} %s\n" "$*" >&2
}

###############################################################################
# Separator
###############################################################################

separator()
{
    printf '%80s\n' '' | tr ' ' '-'
}

###############################################################################
# Sections
###############################################################################

log_step() {

    separator

    printf "%s\n" "$*"

    separator
}

###############################################################################
# Public API
###############################################################################

info() {

    log_info "$@"
}

ok() {

    log_success "$@"
}

warn() {

    log_warn "$@"
}

fail() {

    log_error "$@"

    exit 1
}

###############################################################################
# Headers
###############################################################################

print_section() {

    log_step "$@"
}

#
# Backward compatibility
#

print_header() {

    print_section "$@"
}

###############################################################################
# Footer
###############################################################################

print_footer()
{
    ok "$@"
}