###############################################################################

###############################################################################
# Separator
###############################################################################

separator() {

    printf '%*s\n' 80 '' | tr ' ' '-'
}

###############################################################################
# Root
###############################################################################

require_root() {

    [[ "${EUID}" -eq 0 ]] \
        || fail "This script must be run as root."
}

###############################################################################
# Commands
###############################################################################

require_command() {

    local command="$1"

    command -v "${command}" >/dev/null 2>&1 \
        || fail "Required command not found: ${command}"
}

###############################################################################
# Required commands
###############################################################################

require_git() {

    require_command git
}

require_rsync() {

    require_command rsync
}


###############################################################################
# Files
###############################################################################

require_file() {

    local file="$1"

    [[ -f "${file}" ]] \
        || fail "Missing file: ${file}"
}

###############################################################################
# Directories
###############################################################################

require_directory() {

    local directory="$1"

    [[ -d "${directory}" ]] \
        || fail "Missing directory: ${directory}"
}

###############################################################################
# Files or directories
###############################################################################

require_file_or_directory() {

    local path="$1"

    [[ -e "${path}" ]] \
        || fail "Missing: ${path}"
}

###############################################################################
# Environment
#
# Generic environment required by all Monitoring-Grafana scripts.
###############################################################################

check_environment() {

    require_root

    require_git

    require_rsync

    ok "Environment OK."
}

###############################################################################
# Run script
###############################################################################

run_step() {

    local script="$1"

    print_section "Running ${script}"

    "${SCRIPT_DIR}/${script}"

    ok "${script} completed."
}